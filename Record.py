class Record:

    def __init__(self):
        
        record_id = ""
        name = ""
        content = ""

    def setId(self, id):
        self.record_id = id

    def setName(self, id):
        self.name = id

    def setContent(self, id):
        self.content = id
    
    def getId(self):
        return self.record_id

    def getName(self):
        return self.name

    def getContent(self):
        return self.content

